/**
 * Ski areas (domaines skiables): big linked domains where several of our resorts
 * share one lift pass or massif. These pages group member resorts for internal
 * linking (maillage) and capture domain-level search intent ("Les 3 Vallées
 * hotels", "Portes du Soleil ski-in ski-out", etc.).
 *
 * `members` are destination slugs from data/destinations.json, in display order.
 * `pistesKm` / `lifts` are the authoritative DOMAIN totals (not a sum of members,
 * which would double-count a shared domain). `lat`/`lng` centre the Stay22 map.
 */
export interface SkiArea {
  slug: string
  name: string
  countryCodes: string[]
  flags: string[]
  /** Destination slugs that belong to this domain, in display order. */
  members: string[]
  lat: number
  lng: number
  zoom: number
  /** Authoritative domain total piste kilometres. */
  pistesKm: number
  /** Authoritative domain total lift count. */
  lifts: number
  /** Highest lift-served altitude in the domain, metres. */
  topAltitude: number
  /** Marketing / lift-pass name of the domain. */
  pass: string
  intro: Record<Locale, string>
  description: Record<Locale, string>
}

