export function getSkiArea(slug: string): SkiArea | undefined {
  return SKI_AREAS.find((a) => a.slug === slug)
}

/** The ski area a resort belongs to, if any (used for the resort-page cross-link). */
export function getSkiAreaForResort(resortSlug: string): SkiArea | undefined {
  return SKI_AREAS.find((a) => a.members.includes(resortSlug))
}
